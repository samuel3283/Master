package pe.com.nextel.provisioning.model.vo;

public class Consulta2VO {

	private String codigo;
	private String operador ;
	private String CANT_DE_SP;
	private String CANT_DE_ANS;
	private String CANT_DE_VABDCP;
	private String CANT_DE_RABDCP;
	private String CANT_DE_SVP;
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getOperador() {
		return operador;
	}
	public void setOperador(String operador) {
		this.operador = operador;
	}
	public String getCANT_DE_SP() {
		return CANT_DE_SP;
	}
	public void setCANT_DE_SP(String cant_de_sp) {
		CANT_DE_SP = cant_de_sp;
	}
	public String getCANT_DE_ANS() {
		return CANT_DE_ANS;
	}
	public void setCANT_DE_ANS(String cant_de_ans) {
		CANT_DE_ANS = cant_de_ans;
	}
	public String getCANT_DE_VABDCP() {
		return CANT_DE_VABDCP;
	}
	public void setCANT_DE_VABDCP(String cant_de_vabdcp) {
		CANT_DE_VABDCP = cant_de_vabdcp;
	}
	public String getCANT_DE_RABDCP() {
		return CANT_DE_RABDCP;
	}
	public void setCANT_DE_RABDCP(String cant_de_rabdcp) {
		CANT_DE_RABDCP = cant_de_rabdcp;
	}
	public String getCANT_DE_SVP() {
		return CANT_DE_SVP;
	}
	public void setCANT_DE_SVP(String cant_de_svp) {
		CANT_DE_SVP = cant_de_svp;
	}

	
}
