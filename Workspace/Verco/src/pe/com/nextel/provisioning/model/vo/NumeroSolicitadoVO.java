package pe.com.nextel.provisioning.model.vo;

public class NumeroSolicitadoVO {

	private String operador_origen;
	private String tipo_mensaje;
	private String total;
	
	public String getOperador_origen() {
		return operador_origen;
	}
	
	public void setOperador_origen(String operador_origen) {
		this.operador_origen = operador_origen;
	}
	
	public String getTipo_mensaje() {
		return tipo_mensaje;
	}
	
	public void setTipo_mensaje(String tipo_mensaje) {
		this.tipo_mensaje = tipo_mensaje;
	}
	
	public String getTotal() {
		return total;
	}
	
	public void setTotal(String total) {
		this.total = total;
	}
}
